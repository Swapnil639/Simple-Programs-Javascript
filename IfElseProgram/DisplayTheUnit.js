let number = 100;
console.log("Display the Unit of Number");
console.log("Number = "+number);
if(number == 1){
    console.log("One");
}else if(number == 10){
    console.log("Ten");
}else if(number == 100){
    console.log("Hundred");
}else if(number == 1000){
    console.log("Thousand");
}else if(number == 10000){
    console.log("Ten Thousand");
}